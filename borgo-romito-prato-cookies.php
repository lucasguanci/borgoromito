            La presente informativa (o “cookie policy”) è resa ai sensi dell’art. 13 D.Lgs. 196/2003 - “Codice in materia di protezione dei dati personali”, c.d. “Codice della privacy”, e fornisce informazioni sull'uso dei cookie, su come sono utilizzati dal sito e su come gestirli.<br><br> 

            <strong style="color:rgb(185,0,0)">COSA SONO I COOKIE</strong><br>
            I cookie sono brevi frammenti di testo (lettere e/o numeri) che permettono al server web di memorizzare sul browser informazioni da riutilizzare nel corso della medesima visita al sito (cookie di sessione) o in seguito, anche a distanza di giorni (cookie persistenti). I cookie vengono memorizzati, in base alle preferenze dell'utente, dal singolo browser sullo specifico dispositivo utilizzato (computer, tablet, smartphone).<br><br>
             
            <strong style="color:rgb(185,0,0)">TIPOLOGIE DI COOKIE UTILIZZATE</strong><br>
            Il sito web utilizza cookie tecnici e cookie di terze parti per garantire il corretto funzionamento delle procedure e migliorare l'esperienza di uso.
            Non sono utilizzati cookie di profilazione, cookie cioè volti a creare profili relativi all'utente al fine di inviare messaggi pubblicitari in linea con le preferenze manifestate.<br><br>

            <strong style="color:rgb(185,0,0)">COOKIE TECNICI</strong><br>
            I cookie tecnici sono quelli utilizzati al solo fine di "effettuare la trasmissione di una comunicazione su una rete di comunicazione elettronica, o nella misura strettamente necessaria al fornitore di un servizio della società dell'informazione esplicitamente richiesto dall'abbonato o dall'utente a erogare tale servizio" (cfr. art. 122, comma 1, del Codice). Essi non vengono utilizzati per scopi ulteriori e sono normalmente installati direttamente dal titolare o gestore del sito web. Possono essere suddivisi in cookie di navigazione o di sessione, che garantiscono la normale navigazione e fruizione del sito web; cookie analytics, assimilati ai cookie tecnici laddove utilizzati direttamente dal gestore del sito per raccogliere informazioni, in forma aggregata, sul numero degli utenti e su come questi visitano il sito stesso; cookie di funzionalità, che permettono all'utente la navigazione in funzione di una serie di criteri selezionati al fine di migliorare il servizio reso allo stesso.
            Per l'installazione di tali cookie è richiesta la sola informazione preventiva degli utenti.<br><br>

            <strong style="color:rgb(185,0,0)">COOKIE DI TERZE PARTI</strong><br>
